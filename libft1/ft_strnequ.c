#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	unsigned int	i;

	i = 0;
	if (s1 == NULL || s2 == NULL)
		return (0);
	if (n == 0)
		return (1);
	while (s1[i] == s2[i])
	{
		if (!(s1[i]) || (i >= n - 1))
			return (1);
		i++;
	}
	return (0);
}
